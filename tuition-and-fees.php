                        <h3 class="featured-heading-text2"><b>Application Fee:</b></h3>
                           <p class="featured-heading-textp">There is no application fee for admission to any of our programs.
                             Our admissions team will carefully evaluate your credentials and process your application at no cost to you.
                              If you qualify, we’ll offer you admission, thus allowing you to begin your journey
                               without any upfront financial burden.</p>
                            <br>


                            <h3 class="featured-heading-text2"><b>Tuition Fee:</b></h3>
                           <p class="featured-heading-textp">Our tuition is fully covered by Spirit Brood Theological Seminary.
                             This means your education is tuition-free, and you can focus on deepening your 
                             knowledge and growing in ministry without the pressure of high tuition costs.</p>
                            <br>

                            <h3 class="featured-heading-text2"><b>Acceptance Fee:</b></h3>
                           <p class="featured-heading-textp">Upon admission, students are asked to pay a modest acceptance fee to 
                            signify their commitment to their studies and help us cover essential 
                            administrative costs. This small fee supports your academic journey by 
                            encouraging a commitment to completion, as studies show that students 
                            who invest in their education are more likely to stay the course. This fee also 
                            helps maintain the resources and support necessary for your program.</p>
                            <br>

                            <h3 class="featured-heading-text2"><b>Thesis Fee:</b></h3>
                           <p class="featured-heading-textp">When you reach the thesis phase of your program,
                             a dedicated fee will cover the supervision and evaluation process.
                              This fee ensures that you receive personalized guidance from an experienced academic
                               in your field. This onetime fee is required only when you are ready to undertake your thesis.</p>
                            <br>

                            <h3 class="featured-heading-text2"><b>Graduation Fee:</b></h3>
                           <p class="featured-heading-textp">Upon completing your program, a graduation fee helps cover
                             administrative costs associated with preparing your graduation documents and shipping 
                             them to your location. This ensures a seamless graduation process and 
                             proper documentation for your future endeavors.</p>
                            <br>

                            <h3 class="featured-heading-text2"><b>Student Package (Optional):</b></h3>
                           <p class="featured-heading-textp">For students who wish to receive a Student ID, printed admission documents, 
                            proof of studentship, and other formal records, the optional Student Package 
                            fee covers the preparation and shipping of these items. This is entirely 
                            optional for those who may find it helpful for personal or professional reasons.</p>
                            <br>

                            <h3 class="featured-heading-text2"><b>Mentorship Fee:</b></h3>
                           <p class="featured-heading-textp">This annual fee helps maintain your active student status.
                             It enables us to provide each student with access to a mentor who will serve as a guide 
                             throughout the year. If this fee is not paid, the student will be considered 
                             inactive for that academic year. By staying engaged with your mentor, you’ll enjoy a deeper. Highly effective, and more fulfilling academic experience.</p>
