                        <!-- <h3 class="featured-heading-text2">Spirit Brood Seminary Honors Life Experience.</h3> -->
                        <p class="featured-heading-textp">At Spirit Brood, we evaluate your term papers based on several key criteria.
                             Our grading system ensures that your work is assessed fairly and holistically,
                              focusing on both the quality of your content and your originality.
                               Here’s how it works:
                            <br><br></p>

                            <h3 class="featured-heading-text2"><b>Term Paper Marking Scheme</b></h3>
                             <p class="featured-heading-textp">Your term paper will be scored based on the following ten parameters:</p>
                            <br>



                           
                             <h3 class="featured-heading-text2"><b>1. Argumentation and Analysis (25%)</b></h3>
                             <p class="featured-heading-textp">We assess how well you build your argument and analyze the topic.
                                 This is the foundation of your paper, where we look for solid reasoning, logical 
                                 flow, and how effectively you support your points with evidence.</p>
                            <br>

                            <h3 class="featured-heading-text2"><b>2. Structure and Organization (15%)</b></h3>
                             <p class="featured-heading-textp">Your paper should be logically organized,
                                 with clear sections and a smooth flow from one point to the next.
                                  Good structure makes your arguments easier to follow.</p>
                            <br>

                            <h3 class="featured-heading-text2"><b>3. Originality and Creativity (10%)</b></h3>
                             <p class="featured-heading-textp">We value original thought and creative approaches.
                                 This means presenting ideas in a unique way and showing your own understanding
                                  rather than simply repeating others’ views.</p>

                            <br>

                            <h3 class="featured-heading-text2"><b>4. Relevance to the Topic or Question (15%)</b></h3>
                             <p class="featured-heading-textp">Your content should be directly related to the topic or question given.
                                 Stay on topic and ensure that each point you make connects to the main focus 
                                 of the assignment.</p>
                            <br>

                            <h3 class="featured-heading-text2"><b>5. Use of Sources and Referencing (10%)</b></h3>
                             <p class="featured-heading-textp">Good use of sources strengthens your argument.
                                 We’ll check how well you incorporate external sources and whether you correctly
                                  reference them, giving credit where it’s due.</p>
                            <br>

                            <h3 class="featured-heading-text2"><b>6. Clarity and Precision (10%)</b></h3>
                             <p class="featured-heading-textp">Clarity means your writing is easy to understand,
                                 and precision means avoiding vague language. The more clear and specific you are,
                                  the better.</p>
                            <br>

                            <h3 class="featured-heading-text2"><b>7. Depth of Insight (10%)</b></h3>
                             <p class="featured-heading-textp">This criterion assesses how deeply you engage with the
                                 topic. Show us that you’ve thought about the issue carefully and can provide thoughtful
                                 perspectives.</p>
                            <br>

                            <h3 class="featured-heading-text2"><b>8. Language and Style (5%)</b></h3>
                             <p class="featured-heading-textp">We’ll evaluate your language skills, focusing on grammar,
                                 sentence structure, and overall style. Well-written papers are easier to read and 
                                 more effective in conveying ideas.</p>
                            <br>

                            <h3 class="featured-heading-text2"><b>9. Formatting and Presentation (5%)</b></h3>
                             <p class="featured-heading-textp">Follow the formatting guidelines provided, such as font size,
                                 line spacing, and citation style. Presentation makes your paper look professional.</p>
                            <br>

                            <h3 class="featured-heading-text2"><b>10. Conclusion and Summary (5%)</b></h3>
                             <p class="featured-heading-textp">A strong conclusion neatly wraps up your paper and
                                 reinforces the main arguments. Summarize key points and provide a clear takeaway
                                  for the reader</p>
                            <br>

                            <p class="featured-heading-textp">Each parameter contributes a specific percentage to
                                 your overall grade, with the total reaching 100%.</p> <br><br>

                                 <h3 class="featured-heading-text2"><b>Plagiarism and AI Content Tests</b></h3>
                                 <p class="featured-heading-textp">After scoring your paper based on these
                                     parameters, we run two additional tests:</p>

                                     <h3 class="featured-heading-text2"><b>1. Plagiarism Test</b></h3>
                             <p class="featured-heading-textp">We check the originality of your content to ensure
                                 it’s truly your own work. High levels of originality improve your final score.</p>
                            <br>

                            <h3 class="featured-heading-text2"><b>2. AI Detector Test</b></h3>
                             <p class="featured-heading-textp">This test checks for human versus AI-generated content.
                                 A high human content score also boosts your overall grade.</p>
                            <br><br><br>

                            <h3 class="featured-heading-text2"><b>Final Grade Calculation</b></h3>
                             <p class="featured-heading-textp">To calculate your final grade, we average
                                 the originality scores from the plagiarism and AI content tests,
                                  then multiply that average by your term paper score.
                                   This ensures that your final grade reflects both the quality
                                    and originality of your work.</p>
